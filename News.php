// Load dynamic news content
document.addEventListener('DOMContentLoaded', function() {
  const page = new URLSearchParams(window.location.search).get('page') || 1;
  loadNews(page);
});

function loadNews(page = 1) {
  const container = document.getElementById('newsContainer');
  
  fetch(`backend/routes/news.php?route=published&page=${page}&limit=6`)
    .then(r => r.json())
    .then(data => {
      if (data.success && data.data && data.data.length > 0) {
        // helper to strip HTML and create excerpt
        const stripHTML = (html) => {
          const tmp = document.createElement('div');
          tmp.innerHTML = html || '';
          return (tmp.textContent || tmp.innerText || '').trim();
        };

        let html = '<div class="news-grid">';

        data.data.forEach((news) => {
          const date = new Date(news.published_at).toLocaleDateString('id-ID', { year: 'numeric', month: 'long', day: 'numeric' });
          const imageUrl = news.image_url || 'assets/images/download-2-784x1168.jpeg';
          const text = stripHTML(news.content);
          const preview = text.length > 180 ? text.substring(0, 180).trim() + '...' : text;

          html += `
            <article class="news-card">
              <img src="${imageUrl}" alt="${news.title}">
              <div class="card-body">
                <div class="meta"><span class="badge-cat">${news.category}</span> ${date} • By ${news.author_name}</div>
                <h3>${news.title}</h3>
                <p class="excerpt">${preview}</p>
                <a class="read-more" href="news_detail.php?slug=${news.slug}">Read More</a>
              </div>
            </article>`;
        });

        html += '</div>';

        // Pagination (if needed)
        if (data.pagination && data.pagination.pages > 1) {
          html += '<nav aria-label="Page navigation" style="margin: 28px 0; text-align: center;">';
          html += '<ul class="pagination justify-content-center">';
          for (let i = 1; i <= data.pagination.pages; i++) {
            const active = (i === parseInt(page)) ? 'active' : '';
            html += `<li class="page-item ${active}"><a class="page-link" href="News.php?page=${i}">${i}</a></li>`;
          }
          html += '</ul></nav>';
        }

        container.className = '';
        container.innerHTML = html;
      } else {
        container.className = '';
        container.innerHTML = '<div class="news-empty">No news articles available yet.</div>';
      }
    })
    .catch(err => {
      console.error('Error loading news:', err);
      container.className = '';
      container.innerHTML = '<div class="news-empty" style="color: red;">Error loading news. Please try again later.</div>';
    });
}
</script>
  
  
